    private int kodeunitnumber() {
        String noAwal = null;
        int noSekarang = 0;
        try {
            String sql = "SELECT MAX(no_transaksi) AS sekarang FROM pinjam";
            rs = st.executeQuery(sql);
            if (rs.next()) {
                noAwal = rs.getString("sekarang");
            }

            if (noAwal == null) {
                noSekarang = 1;
            } else {
                noSekarang = Integer.valueOf(noAwal) + 1;
            }
        } catch (Exception e) {
            System.out.println(e);
        }
        return noSekarang;
    }
    
