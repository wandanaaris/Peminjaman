    private void Bcari1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Bcari1ActionPerformed
        try {
            st = cn.createStatement();
            rs = st.executeQuery("SELECT * FROM karyawan where nik ='"+
                Tnik.getText()+"'");
            while (rs.next()){
                Tnamakaryawan.setText(rs.getString(2));

                JOptionPane.showMessageDialog(rootPane, "Ini Datanya Berdasarkan NIK = '"+Tnik.getText()+"' ");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "eror"+e);
        }
    }//GEN-LAST:event_Bcari1ActionPerformed

