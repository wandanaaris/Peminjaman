    private void TBkaryawanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TBkaryawanMouseClicked
        try {
            Tnik.setText(tabModel.getValueAt(TBkaryawan.getSelectedRow(), 0).toString());
            Tnama.setText(tabModel.getValueAt(TBkaryawan.getSelectedRow(), 1).toString());
            Cgol.setSelectedItem(tabModel.getValueAt(TBkaryawan.getSelectedRow(), 2).toString());
            Bsimpan.setEnabled(false);
            Bedit.setEnabled(true);
            Bhapus.setEnabled(true);
            Tnik.setEnabled(false);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_TBkaryawanMouseClicked

    private void BeditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BeditActionPerformed
        if (Tnik.getText().isEmpty() || Tnama.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Lengkapi Data!");
        } else {
            try {
                st = cn.createStatement();
                st.executeUpdate("UPDATE karyawan set nama='"+Tnama.getText()
                    + "', gol='"+Cgol.getSelectedItem()
                    + "' WHERE nik='"+ Tnik.getText()+"'");
                JOptionPane.showMessageDialog(null, "Berhasil Diupdate");
                tampilData();
                reset();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_BeditActionPerformed

    private void BhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BhapusActionPerformed
        int jawab;
        if ((jawab = JOptionPane.showConfirmDialog(null, "Ingin Menghapus Data?", "Konfirmasi", JOptionPane.YES_NO_OPTION)) == 0) {
            try {
                st = cn.createStatement();
                st.executeUpdate("delete from karyawan where nik='" + Tnik.getText()+ "'");
                JOptionPane.showMessageDialog(null, "Berhasil Dihapus");
                tampilData();
                reset();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_BhapusActionPerformed

