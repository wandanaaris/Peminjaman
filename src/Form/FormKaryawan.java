    private void BsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BsimpanActionPerformed
        if (Tnik.getText().isEmpty() || Tnama.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Lengkapi Data!");
        } else {
            try {
                st = cn.createStatement();
                st.executeUpdate("INSERT INTO karyawan VALUES('"+
                    Tnik.getText()+"','"+
                    Tnama.getText()+"','"+
                    Cgol.getSelectedItem()+"')");
                JOptionPane.showMessageDialog(null, "Berhasil Disimpan");
                tampilData();
                reset();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_BsimpanActionPerformed

    private void BcariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BcariActionPerformed
                try {
            st = cn.createStatement();
            rs = st.executeQuery("SELECT * FROM karyawan where nik ='"+
                Tnik.getText()+"'");
            while (rs.next()){

                Tnama.setText(rs.getString(2));
                Cgol.setSelectedItem(rs.getString(3));
                JOptionPane.showMessageDialog(rootPane, "Ini Datanya Berdasarkan NIK = '"+Tnik.getText()+"' ");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "eror"+e);
        }
    }//GEN-LAST:event_BcariActionPerformed

