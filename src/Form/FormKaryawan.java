    public void tampilData(){
        try {
           st = cn.createStatement();
            tabModel.getDataVector().removeAllElements();
            tabModel.fireTableDataChanged();
            rs = st.executeQuery("select * from karyawan");
            while (rs.next()) {
                Object[] data = {
                   rs.getString("nik"),
                   rs.getString("nama"),
                   rs.getString("gol")
                };
                tabModel.addRow(data);
            }
        } catch (Exception e) {
          e.printStackTrace();  
        }
        reset();
    }

